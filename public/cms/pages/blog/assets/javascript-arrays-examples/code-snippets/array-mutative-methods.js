// Description: {sort}
// Mutative: {true}
const input1 = ["e", "d", "c", "b", "a"];
console.log(input1.sort()); // ["a","b","c","d","e"]
console.log(input1); // ["a","b","c","d","e"]

// Description: {reverse}
// Mutative: {true}
const input2 = ["a", "b", "c", "d", "e"];
console.log(input2.reverse()); // ["e","d","c","b","a"]
console.log(input2); // ["e","d","c","b","a"]
